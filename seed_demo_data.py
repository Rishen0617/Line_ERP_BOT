def d(offset: int) -> str:
    """today + offset days → YYYY-MM-DD"""
    return (TODAY + timedelta(days=offset)).isoformat()


def ts(offset: int, hour: int = 9, minute: int = 0) -> str:
    """today + offset days → YYYY-MM-DD HH:MM"""
    dt = datetime(TODAY.year, TODAY.month, TODAY.day, hour, minute, tzinfo=TZ) + timedelta(days=offset)
    return dt.strftime("%Y-%m-%d %H:%M")


def write_sheet(sheet_name: str, rows: list[list]) -> None:
    """Clear existing data (keep header) then append rows."""
    # Read current header
    result = sheets.values().get(
        spreadsheetId=SPREADSHEET_ID,
        range=f"{sheet_name}!1:1",
    ).execute()
    header = result.get("values", [[]])[0]

    # Clear from row 2 onward
    sheets.values().clear(
        spreadsheetId=SPREADSHEET_ID,
        range=f"{sheet_name}!A2:Z9999",
    ).execute()

    if not rows:
        return

    str_rows = [[str(c) if c is not None else "" for c in row] for row in rows]
    sheets.values().append(
        spreadsheetId=SPREADSHEET_ID,
        range=f"{sheet_name}!A2",
        valueInputOption="USER_ENTERED",
        insertDataOption="INSERT_ROWS",
        body={"values": str_rows},
    ).execute()
    print(f"  ✅ {sheet_name}：已寫入 {len(rows)} 筆")



def write(sheet, rows):
    sh.values().clear(spreadsheetId=SID, range=f"{sheet}!A2:Z9999").execute()
    if rows:
        sh.values().append(
            spreadsheetId=SID, range=f"{sheet}!A2",
            valueInputOption="USER_ENTERED",
            insertDataOption="INSERT_ROWS",
            body={"values": [[str(c) if c is not None else "" for c in r] for r in rows]}
        ).execute()
    print(f"  ✅ {sheet}：{len(rows)} 筆")


